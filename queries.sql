USE mydb;

-- ---------------------------------------------------------------------
-- Завдання 1.
--   1a) Вибрати всі стовпчики (за допомогою wildcard "*") з таблиці products.
--   1b) Вибрати тільки стовпчики name, phone з таблиці shippers.
-- ---------------------------------------------------------------------

-- 1a
SELECT *
FROM products;

-- 1b
SELECT name, phone
FROM shippers;

-- ---------------------------------------------------------------------
-- Завдання 2.
-- Знайти середнє, максимальне та мінімальне значення стовпчика price
-- таблиці products.
-- ---------------------------------------------------------------------

SELECT
    AVG(price) AS avg_price,
    MAX(price) AS max_price,
    MIN(price) AS min_price
FROM products;

-- ---------------------------------------------------------------------
-- Завдання 3.
-- Обрати унікальні значення колонок category_id та price з таблиці products.
-- Порядок виведення — за спаданням значення price, обмежити 10 рядками.
-- ---------------------------------------------------------------------

SELECT DISTINCT category_id, price
FROM products
ORDER BY price DESC
LIMIT 10;

-- ---------------------------------------------------------------------
-- Завдання 4.
-- Знайти кількість продуктів (рядків), ціна яких знаходиться у межах
-- від 20 до 100 (включно).
-- ---------------------------------------------------------------------

SELECT COUNT(*) AS products_in_price_range
FROM products
WHERE price BETWEEN 20 AND 100;

-- ---------------------------------------------------------------------
-- Завдання 5.
-- Знайти кількість продуктів (рядків) та середню ціну (price)
-- у кожного постачальника (supplier_id).
-- ---------------------------------------------------------------------

SELECT
    supplier_id,
    COUNT(*)   AS products_count,
    AVG(price) AS avg_price
FROM products
GROUP BY supplier_id
ORDER BY supplier_id;
